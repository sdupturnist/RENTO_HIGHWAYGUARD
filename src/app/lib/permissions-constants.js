export function hasPermission(userRole, allowedRoles) {
    if (!userRole)
        return false;
    const normalizedRole = userRole.toUpperCase();
    if (normalizedRole === "SUPER ADMIN" || normalizedRole === "ADMIN")
        return true;
    return allowedRoles.includes(normalizedRole);
}
